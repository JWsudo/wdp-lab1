#A

print(type(1 + 2)) #dodawanie <class 'int'>
print(type(1 + 4.5)) #dodawanie <class 'float'>
print(type(3 / 2)) #dzielenie zmiennoprzeciwnkowe <class 'float'>
print(type(4 / 2)) #dzielenie zmiennoprzeciwnkowe <class 'float'>
print(type(3 // 2)) #dzielenie calkowite <class 'int'>
print(type(-3 // 2)) #dzielenie calkowite <class 'int'>
print(type(11 % 2)) #modulo <class 'int'>
print(type(2 ** 10)) #potegowanie <class 'int'>
print(type(8 ** (1/3))) #potegowanie i dzielenie zmiennoprzeciwnkowe <class 'float'>


